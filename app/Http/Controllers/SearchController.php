    public function Searching(){
      $q = Input::get ( 'q' );
      $search_book = Book::where('title','LIKE','%'.$q.'%')
      ->orWhere('description','LIKE','%'.$q.'%')
      ->get();
      if ($search_book)
      return view('search')->withDetails($search_book)->withQuery ( $q );
        else
      return view ('homepage')->withMessage('No se encuentran datos');
    }
